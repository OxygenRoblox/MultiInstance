# Define file paths
COOKIE_JSON="/Users/abdifatah/Downloads/cookies.json"
SESSION_FILE="/Users/abdifatah/Downloads/roblox_session.txt"

# Create COOKIE_JSON file with initial content if it does not exist
if [ ! -f "$COOKIE_JSON" ]; then
    echo '{"cookies": ["placeholder_cookie_value"]}' > "$COOKIE_JSON"
    echo "Created $COOKIE_JSON with initial content."
fi

# Function to get all cookies from the JSON file
get_cookies() {
    jq -r '.cookies[]' "$COOKIE_JSON"
}

# Function to update the session file with the current cookie
update_session_file() {
    local cookie="$1"
    echo "$cookie" > "$SESSION_FILE"
}

# Function to cycle through cookies
cycle_cookies() {
    local cookies=($(get_cookies))
    local num_cookies=${#cookies[@]}
    local current_index=0

    while true; do
        # Get the current cookie based on index
        local cookie=${cookies[$current_index]}
        update_session_file "$cookie"

        # Move to the next cookie index
        current_index=$(( (current_index + 1) % num_cookies ))

        # Delay before updating again
        sleep 2  # Adjust the delay as needed
    done
}

# Start cycling through cookies
cycle_cookies
